#include "libft.h"

void	*ft_memset(void *b, int l, size_t n)
{
	int				a;
	unsigned char	*str;

	str = b;
	a = 0;
	while (n > 0)
	{
		str[a] = l;
		n--;
		a++;
	}
	return (str);
}
